async def seed():
    async with aiosqlite.connect(DB_PATH) as db:
        await db.executescript("""
        CREATE TABLE IF NOT EXISTS users (
            tg_id INTEGER PRIMARY KEY,
            username TEXT,
            gender TEXT,
            is_banned INTEGER DEFAULT 0,
            is_premium INTEGER DEFAULT 0,
            premium_until TEXT,
            created_at TEXT DEFAULT (datetime('now')),
            last_active TEXT DEFAULT (datetime('now'))
        );
        CREATE TABLE IF NOT EXISTS profiles (
            tg_id INTEGER PRIMARY KEY,
            name TEXT,
            age INTEGER,
            city TEXT,
            about TEXT,
            is_approved INTEGER DEFAULT 1,
            updated_at TEXT DEFAULT (datetime('now'))
        );
        CREATE TABLE IF NOT EXISTS photos (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            tg_id INTEGER,
            file_id TEXT NOT NULL
        );
        CREATE TABLE IF NOT EXISTS likes (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            from_id INTEGER,
            to_id INTEGER,
            value INTEGER NOT NULL,
            message_text TEXT,
            message_file_id TEXT,
            message_type TEXT,
            created_at TEXT DEFAULT (datetime('now')),
            UNIQUE(from_id, to_id)
        );
        CREATE TABLE IF NOT EXISTS daily_likes (
            tg_id INTEGER,
            day TEXT,
            count INTEGER DEFAULT 0,
            PRIMARY KEY (tg_id, day)
        );
        CREATE TABLE IF NOT EXISTS weekly_likes (
            tg_id INTEGER,
            week TEXT,
            count INTEGER DEFAULT 0,
            PRIMARY KEY (tg_id, week)
        );
        CREATE TABLE IF NOT EXISTS viewed (
            viewer_id INTEGER,
            target_id INTEGER,
            viewed_at TEXT DEFAULT (datetime('now')),
            PRIMARY KEY (viewer_id, target_id)
        );
        """)

        inserted_users = 0
        inserted_profiles = 0

        all_profiles = [
            (p, "male") for p in MALE_PROFILES
        ] + [
            (p, "female") for p in FEMALE_PROFILES
        ]

        for profile_data, gender in all_profiles:
            tg_id = profile_data["tg_id"]

            # Пропускаем если уже существует
            async with db.execute("SELECT tg_id FROM users WHERE tg_id=?", (tg_id,)) as cur:
                if await cur.fetchone():
                    print(f"⏭  Пропуск {profile_data['name']} (уже есть)")
                    continue

            await db.execute("""
                INSERT OR IGNORE INTO users (tg_id, username, gender, is_premium)
                VALUES (?, ?, ?, ?)
            """, (tg_id, profile_data["username"], gender, 0))

            await db.execute("""
                INSERT OR IGNORE INTO profiles (tg_id, name, age, city, about)
                VALUES (?, ?, ?, ?, ?)
            """, (tg_id, profile_data["name"], profile_data["age"],
                  profile_data["city"], profile_data["about"]))

            inserted_users += 1
            inserted_profiles += 1
            print(f"✅ {'👨' if gender == 'male' else '👩'} {profile_data['name']}, "
                  f"{profile_data['age']} лет, {profile_data['city']}")

        await db.commit()

        print(f"\n{'='*40}")
        print(f"✅ Добавлено пользователей: {inserted_users}")
        print(f"✅ Добавлено анкет: {inserted_profiles}")
        print(f"👨 Мужских: {sum(1 for _, g in all_profiles if g == 'male')}")
        print(f"👩 Женских: {sum(1 for _, g in all_profiles if g == 'female')}")
        print(f"{'='*40}")
        print("Бот готов к тестированию! 🌙")


if __name__ == "__main__":
    asyncio.run(seed())
